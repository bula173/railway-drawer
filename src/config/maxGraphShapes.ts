    decision: 'rhombus=1;strokeColor=#999;fillColor=#fff9e6;strokeWidth=1',
    database: 'ellipse=1;strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
    document: 'rounded=0;strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
