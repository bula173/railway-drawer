/**
 * Get all shapes flattened into a single array
 */
export function getAllMaxGraphShapes(): ToolboxItem[] {
  return Object.values(MAX_GRAPH_SHAPES).flat();
}

/**
 * Get shapes by category
 */
export function getShapesByCategory(category: string): ToolboxItem[] {
  return MAX_GRAPH_SHAPES[category] || [];
}

/**
 * Get all available categories
 */
export function getShapeCategories(): string[] {
  return Object.keys(MAX_GRAPH_SHAPES);
}

/**
 * Find shape by ID
 */
export function findShapeById(id: string): ToolboxItem | undefined {
  return getAllMaxGraphShapes().find(shape => shape.id === id);
}

