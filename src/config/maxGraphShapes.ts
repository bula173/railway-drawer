    diamond: 'rhombus=1;strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
    triangle: 'triangle=1;strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
    hexagon: 'hexagon=1;strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
    pentagon: 'polygon=1;points=[[0.5,0],[1,0.4],[0.8,1],[0.2,1],[0,0.4]];strokeColor=#999;fillColor=#f0f0f0;strokeWidth=1',
