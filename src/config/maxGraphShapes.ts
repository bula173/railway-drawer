/**
 * Get all shapes with their maxGraph styles
 */
export function getAllShapesWithStyles(): Array<ToolboxItem & { style: string }> {
  return getAllMaxGraphShapes().map(shape => ({
    ...shape,
    style: getMaxGraphShapeStyle(shape.id),
  }));
}
